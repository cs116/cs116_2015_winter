// filename: datatype.cpp

#include <iostream>
using namespace std;

int main ()
{
  int variable1 = 97;
  int variable2 = 98;
  char c = 97;
  char d = 'd';

  variable1 = d; 
  cout << "variable1: " << variable1 << endl;
  cout << "variable2: " << variable2 << endl;
  cout << "c: " << c << endl;
  cout << "d: " << d << endl;

  return 0;
}
